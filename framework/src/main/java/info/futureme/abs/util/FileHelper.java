        /**
         * Copy a file into another
         * @param from
         * @param to
         */
        public static void copyFile(String from, String to) throws IOException{
            int BUFF_SIZE = 100000;
            byte[] buffer = new byte[BUFF_SIZE];

            InputStream src = null;
            OutputStream dst = null;

            try {
                src = new FileInputStream(from);
                dst = new FileOutputStream(to);
                while (true) {
                    int amountRead = src.read(buffer);
                    if (amountRead == -1) {
                        break;
                    }
                    dst.write(buffer, 0, amountRead);
                }
            } finally {
                if (src != null) {
                    src.close();
                }
                if (dst != null) {
                    dst.close();
                }
            }
        }


    public static boolean deletePath(String path) {
        File dir = new File(path);
        if (dir.isDirectory()) {
            String[] children = dir.list();
            for (int i = 0; i < children.length; i++) {
                if(!new File(dir, children[i]).delete())
                    return false;
            }
        }
        return true;
    }


