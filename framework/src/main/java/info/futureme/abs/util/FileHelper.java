    /**
     * Build a file path
     */
    public static String filePath(String... parts) {
        if (parts.length == 0) {
            return "";
        }

        StringBuilder sb = new StringBuilder();
        int end = parts.length - 1;
        for (int i = 0; i < end; ++i) {
            sb.append(parts[i]);
            sb.append(File.separator);
        }
        sb.append(parts[end]);
        return sb.toString();
    }


    public static String getHashAbleFileName(String filePath, String suffix){
        String filename = splitFileName(filePath)[1];
        if("".equals(filename)|| filename.indexOf(".")<0){
            filePath = filePath.concat(".bin");
            filename = splitFileName(filePath)[1];
        }
        return new MD5(filename.substring(0, filename.lastIndexOf("."))).getMD5()
                + "_" + System.currentTimeMillis()
                + (suffix == null ? "" : suffix)
                + filename.substring(filename.lastIndexOf("."));
    }

